    public ArrayList<GetLocalidade> ListaLocalidade(){
        ArrayList<GetLocalidade> ListaLocalidade = new ArrayList<>();
        String sql = "select * from localidade";
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            GetLocalidade localidade;
            while(rs.next()){
                localidade = new GetLocalidade(rs.getString("id_localidade"),rs.getString("ds_bairro"),rs.getString("ds_cidade"),rs.getString("ds_estado"),rs.getString("ds_uf"),rs.getString("ds_pais"));
                ListaLocalidade.add(localidade);
            }
        }
        catch(Exception e){
            
        }
        return ListaLocalidade;
    }
    
    public void mostra_lista(){
        ArrayList<GetLocalidade> lista = ListaLocalidade();
        DefaultTableModel modelo = (DefaultTableModel)Tabela.getModel();
        Object[] campos = new Object[6];
        modelo.setRowCount(0);
        for(int i=0;i<lista.size();i++){
            campos[0] = lista.get(i).GetGen();
            campos[1] = lista.get(i).GetBairro();
            campos[2] = lista.get(i).GetCidade();
            campos[3] = lista.get(i).GetEstado();
            campos[4] = lista.get(i).GetUF();
            campos[5] = lista.get(i).GetPais();
            modelo.addRow(campos);
        }
    }

