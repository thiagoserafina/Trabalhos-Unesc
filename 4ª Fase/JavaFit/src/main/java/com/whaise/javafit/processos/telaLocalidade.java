    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        String sql_gerador = "select * from LOCALIDADe order by ID_LOCALIDADE DESC";
        String sql_inserir = "insert into LOCALIDADE values(?,?,?,?,?,?)";
        String gen;
        if (edtBairro.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Bairro Vazio");
        }
        else if (edtCidade.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Cidade Vazio");
        }
        else if (edtEstado.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(null, "Estado Vazio");
        }
        else {
            try{
                pst = con.prepareStatement(sql_gerador);
                rs = pst.executeQuery();
                if(rs.next()){
                    gen = Integer.toString(rs.getInt("id_localidade")+1);
                }
                else {
                    gen = "1";
                }
                pst = con.prepareStatement(sql_inserir);
                pst.setString(1, gen);
                pst.setString(2, edtBairro.getText());
                pst.setString(3, edtCidade.getText());
                pst.setString(4, edtEstado.getSelectedItem().toString());
                pst.setString(5, edtUF.getText());
                pst.setString(6, edtPais.getText());
                pst.executeUpdate();
                mostra_lista();
                edtBairro.setText("");
                edtCidade.setText("");
                edtEstado.setSelectedIndex(-1);
                edtUF.setText("");
                edtPais.setText("");
            }
            catch(Exception e){
              System.out.println("Erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

